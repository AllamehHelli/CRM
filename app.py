from flask import Flask

# ساخت یک نمونه از اپلیکیشن وب با استفاده از فلسک
app = Flask(__name__)

# تعریف یک مسیر (Route) برای صفحه اصلی وب‌سایت
# وقتی کسی به آدرس اصلی سایت ما مراجعه کند، این تابع اجرا می‌شود
@app.route('/')
def index():
    # متنی که به عنوان پاسخ در مرورگر نمایش داده می‌شود
    return "<h1>سیستم مدیریت تیکت علامه حلی - نسخه اولیه</h1>"
    
# این بخش اجازه می‌دهد تا در آینده بتوانیم برنامه را مستقیماً روی کامپیوتر خودمان هم اجرا کنیم
# اما در محیط Render، gunicorn مستقیماً متغیر 'app' را پیدا و اجرا می‌کند
if __name__ == '__main__':
    app.run(debug=True)
